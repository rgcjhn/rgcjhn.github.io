import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

// Get __dirname equivalent in ES modules
const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// Configuration
const ASSETS_DIR = path.join(__dirname, '../src/assets');
const OUTPUT_FILE = path.join(__dirname, '../src/assets/index.ts');
const VALID_EXTENSIONS = [
  '.png',
  '.jpg',
  '.jpeg',
  '.gif',
  '.svg',
  '.webp',
  '.bmp',
  '.ico',
  '.tiff',
];

function generateImageIndex() {
  console.log('📁 Scanning assets directory:', ASSETS_DIR);

  try {
    // Check if assets directory exists
    if (!fs.existsSync(ASSETS_DIR)) {
      console.error('❌ Assets directory not found:', ASSETS_DIR);
      process.exit(1);
    }

    // Read all files in the assets directory
    const files = fs.readdirSync(ASSETS_DIR);

    // Filter image/SVG files and exclude index.ts itself
    const imageFiles = files.filter((file) => {
      const ext = path.extname(file).toLowerCase();
      return VALID_EXTENSIONS.includes(ext) && file !== 'index.ts';
    });

    if (imageFiles.length === 0) {
      console.log('⚠️ No image files found in assets directory');
      return;
    }

    console.log(`✅ Found ${imageFiles.length} image files`);

    // Generate import statements
    const imports = [];
    const exports = [];

    imageFiles.forEach((file) => {
      // Remove extension for import name
      const name = path.basename(file, path.extname(file));
      // Convert to valid variable name (camelCase)
      const varName = toCamelCase(name);

      // Generate import statement
      imports.push(`import ${varName} from './${file}';`);
      exports.push(`  ${varName},`);
    });

    // Create the complete content
    const content = `// Auto-generated by generate-image-index.js
// Last generated: ${new Date().toISOString()}

import type { FC, SVGProps } from 'react';
${imports.join('\n')}

export {
${exports.join('\n')}
};

// TypeScript exports for better type safety
export type ImageAssets = {
${imageFiles
  .map((file) => {
    const name = path.basename(file, path.extname(file));
    const varName = toCamelCase(name);
    const ext = path.extname(file).toLowerCase();
    return `  ${varName}: string${ext === '.svg' ? ' | React.FC<React.SVGProps<SVGSVGElement>>' : ''};`;
  })
  .join('\n')}
};

// Helper function to get asset by name
export function getAsset(name: string): string | FC<SVGProps<SVGSVGElement>> {
  const assets: Record<string, string | FC<SVGProps<SVGSVGElement>>> = {
${imageFiles
  .map((file) => {
    const name = path.basename(file, path.extname(file));
    const varName = toCamelCase(name);
    return `    '${name.toLowerCase()}': ${varName},`;
  })
  .join('\n')}
  };
  
  return assets[name.toLowerCase()];
}

// Utility function to get all asset names
export function getAllAssetNames(): string[] {
  return [
${imageFiles
  .map((file) => {
    const name = path.basename(file, path.extname(file));
    return `    '${name}',`;
  })
  .join('\n')}
  ];
}
`;

    // Write to file
    fs.writeFileSync(OUTPUT_FILE, content, 'utf8');
    console.log(`✅ Successfully generated ${OUTPUT_FILE}`);

    // Log the created imports
    console.log('\n📦 Generated imports:');
    imageFiles.forEach((file) => {
      const name = path.basename(file, path.extname(file));
      const varName = toCamelCase(name);
      console.log(`  - ${varName} from './${file}'`);
    });
  } catch (error) {
    console.error('❌ Error generating image index:', error);
    process.exit(1);
  }
}

// Helper function to convert filename to camelCase
function toCamelCase(str) {
  // Handle special characters and convert to PascalCase
  return (
    str
      // Replace non-alphanumeric characters with spaces
      .replace(/[^a-zA-Z0-9]/g, ' ')
      // Split into words
      .split(' ')
      // Filter out empty strings
      .filter((word) => word.length > 0)
      // Capitalize each word
      .map((word, index) => {
        // Handle numbers at the beginning
        if (/^\d/.test(word)) {
          return '_' + word;
        }
        return word.charAt(0).toUpperCase() + word.slice(1).toLowerCase();
      })
      // Join without spaces (PascalCase)
      .join('')
  );
}

// Run the script
generateImageIndex();
